CREATE TABLE Engineers (
    FID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    Lastname VARCHAR2(30),
    Firstname VARCHAR2(30),
    Email VARCHAR2(30),
    Graddate DATE
);

CREATE TABLE FACULTY (
    FID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    Lastname VARCHAR2(30),
    Firstname VARCHAR2(30),
    Email VARCHAR2(30),
    Hiredate DATE
);